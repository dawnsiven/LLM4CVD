import argparse
import csv
import json
from pathlib import Path
from typing import Dict, List

from config_utils import load_env_file


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate direct LLM predictions on a full test set."
    )
    parser.add_argument(
        "--llm_predictions_csv",
        required=True,
        help="Path to llm_predictions.csv generated by llm_api_judge.py.",
    )
    parser.add_argument(
        "--output_dir",
        default=None,
        help="Directory for evaluation outputs. Defaults to the CSV parent directory.",
    )
    parser.add_argument(
        "--env_file",
        default="LLM_TEST/.env",
        help="Optional env file for consistency with other LLM_TEST scripts.",
    )
    return parser.parse_args()


def load_csv(path: Path) -> List[dict]:
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def to_int(value: object, default: int = 0) -> int:
    try:
        return int(str(value).strip())
    except (TypeError, ValueError):
        return default


def compute_metrics(labels: List[int], preds: List[int]) -> Dict[str, float]:
    tp = sum(1 for y, p in zip(labels, preds) if y == 1 and p == 1)
    tn = sum(1 for y, p in zip(labels, preds) if y == 0 and p == 0)
    fp = sum(1 for y, p in zip(labels, preds) if y == 0 and p == 1)
    fn = sum(1 for y, p in zip(labels, preds) if y == 1 and p == 0)

    total = len(labels)
    accuracy = (tp + tn) / total if total else 0.0
    precision = tp / (tp + fp) if (tp + fp) else 0.0
    recall = tp / (tp + fn) if (tp + fn) else 0.0
    f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) else 0.0
    fpr = fp / (fp + tn) if (fp + tn) else 0.0

    return {
        "accuracy": accuracy,
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "fpr": fpr,
        "tp": tp,
        "tn": tn,
        "fp": fp,
        "fn": fn,
        "total": total,
    }


def safe_write_json(path: Path, payload: object) -> None:
    with path.open("w", encoding="utf-8") as handle:
        json.dump(payload, handle, ensure_ascii=False, indent=2)


def safe_write_csv(path: Path, rows: List[dict], fieldnames: List[str]) -> None:
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def main() -> None:
    args = parse_args()
    load_env_file(args.env_file)

    llm_predictions_csv = Path(args.llm_predictions_csv).resolve()
    if not llm_predictions_csv.exists():
        raise FileNotFoundError(f"LLM predictions CSV not found: {llm_predictions_csv}")

    output_dir = (
        Path(args.output_dir).resolve() if args.output_dir else llm_predictions_csv.parent.resolve()
    )
    output_dir.mkdir(parents=True, exist_ok=True)

    rows = load_csv(llm_predictions_csv)
    valid_rows = [row for row in rows if str(row.get("LLMPrediction", "")).strip() in {"0", "1"}]
    invalid_rows = [row for row in rows if row not in valid_rows]

    labels = [to_int(row.get("Label")) for row in valid_rows]
    preds = [to_int(row.get("LLMPrediction")) for row in valid_rows]
    metrics = compute_metrics(labels, preds)
    metrics.update(
        {
            "scope": "full_test_llm_only",
            "description": "Metrics for direct LLM detection on the full prepared test set.",
            "csv_path": str(llm_predictions_csv),
            "valid_predictions": len(valid_rows),
            "invalid_or_missing_predictions": len(invalid_rows),
            "positive_labels": sum(label == 1 for label in labels),
            "negative_labels": sum(label == 0 for label in labels),
            "positive_predictions": sum(pred == 1 for pred in preds),
            "negative_predictions": sum(pred == 0 for pred in preds),
        }
    )

    invalid_summary_rows = [
        {
            "Index": row.get("Index", ""),
            "Label": row.get("Label", ""),
            "OriginalPrediction": row.get("OriginalPrediction", ""),
            "LLMPrediction": row.get("LLMPrediction", ""),
            "ParseStatus": row.get("ParseStatus", ""),
        }
        for row in invalid_rows
    ]

    safe_write_json(output_dir / "full_llm_metrics.json", metrics)
    safe_write_csv(
        output_dir / "invalid_llm_predictions.csv",
        invalid_summary_rows,
        ["Index", "Label", "OriginalPrediction", "LLMPrediction", "ParseStatus"],
    )

    print(f"llm_predictions_csv={llm_predictions_csv}")
    print(f"valid_predictions={len(valid_rows)}")
    print(f"invalid_or_missing_predictions={len(invalid_rows)}")
    print(json.dumps(metrics, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
